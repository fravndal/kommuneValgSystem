


$("#sidenav").hover(
    function(){
        $("#calendarText").css("display", "inline");
        $("#worksheetText").css("display", "inline");
        $("#chatText").css("display", "inline");
        $("#administratorText").css("display", "inline");
    },
    function () {
        $("#calendarText").css("display", "none");
        $("#worksheetText").css("display", "none");
        $("#chatText").css("display", "none");
        $("#administratorText").css("display", "none");
    });










